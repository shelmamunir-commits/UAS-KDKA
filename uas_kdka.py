def manual_train_test_split(X, y, test_size=0.2, seed=42):
    random.seed(seed)
    combined = list(zip(X, y))
    random.shuffle(combined)
    test_count = int(len(combined) * test_size)
    test_data = combined[:test_count]
    train_data = combined[test_count:]
    X_train, y_train = zip(*train_data)
    X_test, y_test = zip(*test_data)
    return list(X_train), list(X_test), list(y_train), list(y_test)

def calculate_gini(y_labels):
    if not y_labels:
        return 0
    counts = Counter(y_labels)
    total = len(y_labels)
    gini = 1.0
    for label in counts:
        p = counts[label] / total
        gini -= p ** 2
    return gini

def split_dataset(X, y, feature_index, threshold):
    left_X, left_y, right_X, right_y = [], [], [], []
    for i in range(len(X)):
        if X[i][feature_index] <= threshold:
            left_X.append(X[i])
            left_y.append(y[i])
        else:
            right_X.append(X[i])
            right_y.append(y[i])
    return left_X, left_y, right_X, right_y

def get_best_split(X, y, feature_indices):
    best_gini = 999
    best_criteria = None
    best_sets = None
    for feat_idx in feature_indices:
        thresholds = set(row[feat_idx] for row in X)
        for val in thresholds:
            l_X, l_y, r_X, r_y = split_dataset(X, y, feat_idx, val)
            if len(l_y) == 0 or len(r_y) == 0:
                continue
            p_left = len(l_y) / len(y)
            p_right = len(r_y) / len(y)
            gini_after = (p_left * calculate_gini(l_y)) + (p_right * calculate_gini(r_y))
            if gini_after < best_gini:
                best_gini = gini_after
                best_criteria = {'feature': feat_idx, 'threshold': val}
                best_sets = (l_X, l_y, r_X, r_y)
    return best_criteria, best_sets

def build_tree(X, y, max_depth=10, min_samples_split=5, depth=0, n_features=None):
    if (
        len(set(y)) == 1 or
        len(y) < min_samples_split or
        (max_depth is not None and depth >= max_depth)
    ):
        return {'leaf': True, 'label': Counter(y).most_common(1)[0][0]}

    total_features = len(X[0])
    if n_features is None:
        feature_indices = list(range(total_features))
    else:
        feature_indices = random.sample(range(total_features), min(n_features, total_features))

    best_criteria, best_sets = get_best_split(X, y, feature_indices)

    if best_criteria is None:
        return {'leaf': True, 'label': Counter(y).most_common(1)[0][0]}

    l_X, l_y, r_X, r_y = best_sets
    left_branch  = build_tree(l_X, l_y, max_depth, min_samples_split, depth + 1, n_features)
    right_branch = build_tree(r_X, r_y, max_depth, min_samples_split, depth + 1, n_features)

    return {
        'leaf'     : False,
        'feature'  : best_criteria['feature'],
        'threshold': best_criteria['threshold'],
        'left'     : left_branch,
        'right'    : right_branch,
    }

def predict_one(tree, x):
    if tree['leaf']:
        return tree['label']
    if x[tree['feature']] <= tree['threshold']:
        return predict_one(tree['left'], x)
    return predict_one(tree['right'], x)

def predict_tree(tree, X):
    return [predict_one(tree, x) for x in X]

def bootstrap_sample(X, y, seed=None):
    if seed is not None:
        random.seed(seed)
    n = len(X)
    indices = [random.randint(0, n - 1) for _ in range(n)]
    X_boot = [X[i] for i in indices]
    y_boot = [y[i] for i in indices]
    return X_boot, y_boot

def build_random_forest(X, y, n_estimators=30, max_depth=10,
                        min_samples_split=5, n_features=None, seed=42):
    trees = []
    total_features = len(X[0])
    if n_features is None:
        n_features = max(1, int(total_features ** 0.5))

    for i in range(n_estimators):
        X_boot, y_boot = bootstrap_sample(X, y, seed=seed + i)
        tree = build_tree(X_boot, y_boot, max_depth, min_samples_split, n_features=n_features)
        trees.append(tree)
        print(f"  Tree {i+1}/{n_estimators} selesai", end="\r")
    print()
    return trees

def predict_random_forest(trees, X):
    all_preds = [predict_tree(tree, X) for tree in trees]
    final_preds = []
    for i in range(len(X)):
        votes = [all_preds[t][i] for t in range(len(trees))]
        final_preds.append(Counter(votes).most_common(1)[0][0])
    return final_preds

def oversample(X, y, seed=42):
    random.seed(seed)
    counts = Counter(y)
    max_count = max(counts.values())
    X_out, y_out = list(X), list(y)
    for label, count in counts.items():
        if count < max_count:
            indices = [i for i, lbl in enumerate(y) if lbl == label]
            needed = max_count - count
            for _ in range(needed):
                idx = random.choice(indices)
                X_out.append(X[idx])
                y_out.append(y[idx])
    combined = list(zip(X_out, y_out))
    random.shuffle(combined)
    X_out, y_out = zip(*combined)
    return list(X_out), list(y_out)

def evaluate(y_true, y_pred, model_name="Model"):
    labels = sorted(set(y_true))
    correct = sum(1 for t, p in zip(y_true, y_pred) if t == p)
    accuracy = correct / len(y_true)

    recall_per_class = []
    for lbl in labels:
        tp = sum(1 for t, p in zip(y_true, y_pred) if t == lbl and p == lbl)
        fn = sum(1 for t, p in zip(y_true, y_pred) if t == lbl and p != lbl)
        recall = tp / (tp + fn) if (tp + fn) > 0 else 0
        recall_per_class.append(recall)
    balanced_acc = sum(recall_per_class) / len(recall_per_class)

    print(f"EVALUASI {model_name}")
    print(f"Accuracy          : {accuracy:.4f}")
    print(f"Balanced Accuracy : {balanced_acc:.4f}")
    print(f"\n{'Kelas':<35} {'Precision':>10} {'Recall':>10} {'F1':>10} {'Support':>10}")
    for lbl in labels:
        tp = sum(1 for t, p in zip(y_true, y_pred) if t == lbl and p == lbl)
        fp = sum(1 for t, p in zip(y_true, y_pred) if t != lbl and p == lbl)
        fn = sum(1 for t, p in zip(y_true, y_pred) if t == lbl and p != lbl)
        support = sum(1 for t in y_true if t == lbl)
        precision = tp / (tp + fp) if (tp + fp) > 0 else 0
        recall    = tp / (tp + fn) if (tp + fn) > 0 else 0
        f1        = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0
        print(f"{lbl:<35} {precision:>10.4f} {recall:>10.4f} {f1:>10.4f} {support:>10}")

    return accuracy, balanced_acc

